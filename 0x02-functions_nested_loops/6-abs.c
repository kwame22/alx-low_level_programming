#include "main.h"
/**
 * _abs - prints absolute value
 * @int: function accepts an int value
 *
 * Description: prints the absolute value
 * of an integer from input
 * Return: 0
 */
int _abs(int)
{
	int abs_value;

	if (int < 0)
	{
		abs_value = int * -1;
		_putchar(abs_value + '0');
	}
	else
	{
		abs_value = int;
		_putchar(abs_value + '0');
	}
	return (0);
}
